package entidad;

public class EntInformeBasicoCurso {

	private String cantAlumnos;
	private String cantAlumnosAprobados;
	private String cantAlumnosDesaprobados;
	private String profesor;
	private String materia;
	private String cantCursos;
	
	public String getCantCursos() {
		return cantCursos;
	}
	public void setCantCursos(String cantCursos) {
		this.cantCursos = cantCursos;
	}
	public String getMateria() {
		return materia;
	}
	public void setMateria(String materia) {
		this.materia = materia;
	}
	public String getCantAlumnos() {
		return cantAlumnos;
	}
	public void setCantAlumnos(String cantAlumnos) {
		this.cantAlumnos = cantAlumnos;
	}
	public String getCantAlumnosAprobados() {
		return cantAlumnosAprobados;
	}
	public void setCantAlumnosAprobados(String cantAlumnosAprobados) {
		this.cantAlumnosAprobados = cantAlumnosAprobados;
	}
	public String getCantAlumnosDesaprobados() {
		return cantAlumnosDesaprobados;
	}
	public void setCantAlumnosDesaprobados(String cantAlumnosDesaprobados) {
		this.cantAlumnosDesaprobados = cantAlumnosDesaprobados;
	}
	public String getProfesor() {
		return profesor;
	}
	public void setProfesor(String profesor) {
		this.profesor = profesor;
	}
	
	public EntInformeBasicoCurso() {}
	
	
	
	
}
